function degreesToRadians(degrees) {
  return degrees * (Math.PI / 180);
}



export function calculateDistanceBetweenStations(code1, code2) {
  // Find the station objects based on the codes
  const station1 = rajasthanStations.find(station => station.code === code1.toUpperCase());
  const station2 = rajasthanStations.find(station => station.code === code2.toUpperCase());

  // Handle cases where a station code is not found
  if (!station1 || !station2) {
    console.error("One or both station codes not found.");
    return null;
  }

  // Extract and convert coordinates to radians
  const lat1 = degreesToRadians(station1.latitude);
  const lon1 = degreesToRadians(station1.longitude);
  const lat2 = degreesToRadians(station2.latitude);
  const lon2 = degreesToRadians(station2.longitude);

  // Haversine formula implementation
  const R = 6371; // Radius of Earth in kilometers
    const dLat = lat2 - lat1;
  const dLon = lon2 - lon1;

  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos(lat1) * Math.cos(lat2) *
    Math.sin(dLon / 2) * Math.sin(dLon / 2);

  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  const distance = R * c;

  return distance;
}